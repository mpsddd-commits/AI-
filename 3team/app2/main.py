# def llm_call_structured(prompt: str, model: str = "gemma3:4b") -> GraphResponse:
# def llm_call_structured(prompt: str, model: str = "codestral") -> GraphResponse:
def llm_call_structured(prompt: str, model: str = "llama3.2") -> GraphResponse:
# def llm_call_structured(prompt: str, model: str = "mistral-nemo") -> GraphResponse:

  final_prompt = prompt + """
  Return ONLY valid JSON. Do NOT include explanations or commentary.
  """

  # Ollama에 LLM 요청
  response = ollama.chat(
    model=model,
    messages=[{"role": "user", "content": final_prompt}]
  )

  # 모델 응답 텍스트 추출
  text = response["message"]["content"]

  cleaned_text = clean_json_string(text)
  # JSON 파싱 시도
  try:
    parsed = json.loads(cleaned_text)
    # parsed = json.loads(text)
    # print(parsed)

    # 1. 만약 결과가 리스트라면?
    if isinstance(parsed, list):
      # 리스트 내부에 이미 nodes/relationships 구조가 있는 객체가 하나 포함된 경우
      if len(parsed) > 0 and isinstance(parsed[0], dict) and "nodes" in parsed[0]:
        parsed = parsed[0]
      else:
        # 혹은 모델이 노드 리스트만 보냈다고 가정하고 빈 구조를 만듦 (방어적 코드)
        print("  - 주의: 응답이 딕셔너리가 아닌 리스트로 들어왔습니다.")
        parsed = {"nodes": [], "relationships": []}

    # 2. 필수 키가 없는 경우 빈 리스트로 초기화 (KeyError 방지)
    if "nodes" not in parsed: parsed["nodes"] = []
    if "relationships" not in parsed: parsed["relationships"] = []

  except json.JSONDecodeError:
    # 전체 텍스트에서 JSON 블록만 추출
    json_text = re.search(r"\{.*\}", text, re.S)
    if not json_text:
      raise Exception("모델 응답에서 JSON을 찾지 못했습니다.")
    parsed = json.loads(json_text.group(0))
  
  return GraphResponse(**parsed)  # pydantic 모델로 변환 후 반환

# ------------------------------------------------------
# 여러 에피소드 그래프를 통합하기 위한 함수
# ------------------------------------------------------
def combine_chunk_graphs(chunk_graphs: list) -> GraphResponse:
  all_nodes = []  # 모든 노드를 담을 리스트
  for chunk_graph in chunk_graphs:
    for node in chunk_graph.nodes:
      all_nodes.append(node)
  
  all_relationships = []  # 모든 관계를 담을 리스트
  for chunk_graph in chunk_graphs:
    for relationship in chunk_graph.relationships:
      all_relationships.append(relationship)
  
  unique_nodes = []  # 중복 제거된 최종 노드 리스트
  seen = set()  # 노드 중복 체크용

  for node in all_nodes:
    node_key = (node.id, node.label, str(node.properties))  # 노드 고유값 생성
    if node_key not in seen:
      unique_nodes.append(node)
      seen.add(node_key)

  return GraphResponse(nodes=unique_nodes, relationships=all_relationships)

# -------------------------------------------------------------------
# 1. JSON 정제 함수
# -------------------------------------------------------------------
def clean_json_string(json_str: str) -> str:
    """마크다운 태그 및 사소한 JSON 오타를 정리합니다."""
    if not json_str:
        return "{}"
    else:
      # 마크다운 블록 제거
      json_str = re.sub(r'^```json\n', '', json_str, flags=re.MULTILINE)
      json_str = re.sub(r'\n```$', '', json_str, flags=re.MULTILINE)
      return json_str

